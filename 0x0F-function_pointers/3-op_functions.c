#include "3-calc.h"
/**
 *op_add - function declaration
 *
 * @a: first number
 * @b: second number
 *
 * Return: result of the sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - function declaration
 *
 * @a: first number
 * @b: second number
 *
 * Return: result the difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - function declaration
 *
 * @a: first number
 * @b: second number
 *
 * Return: result the product
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - function declaration
 *
 * @a: first number
 * @b: second number
 *
 * Return: result of the division
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - function declaration
 *
 * @a: first number
 * @b: second number
 *
 * Return: result of the remainder of the division
*/
int op_mod(int a, int b)
{
	return (a % b);
}
